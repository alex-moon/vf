  public static get(hash: number) {
    let hex = hash.toString(16);
    if (!(hex.length % 2)) {
      hex = '0' + hex;
    }
    let result = '';
    for (let i = 0; i < hex.length; i += 2) {
      const pair = hex[i] + hex[i + 1];
      const index = parseInt(pair, 16);
      result += NameHelper.syllables[index];
    }
    return result;
  }

